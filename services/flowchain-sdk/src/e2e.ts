function asRecord(value: JsonValue): Record<string, JsonValue> {
  if (value === null || typeof value !== "object" || Array.isArray(value)) return {};
  return value as Record<string, JsonValue>;
}

function asArray(value: JsonValue): JsonValue[] {
  return Array.isArray(value) ? value : [];
}

function stringValue(value: JsonValue): string | null {
  return typeof value === "string" || typeof value === "number" ? String(value) : null;
}

function numberValue(value: JsonValue): number {
  return typeof value === "number" ? value : Number.parseInt(String(value ?? "0"), 10);
}

function heightValue(status: Record<string, JsonValue>): bigint | null {
  const height = stringValue(status.currentBlock ?? status.blockHeight ?? status.latestHeight ?? status.height ?? null);
  if (height === null || !/^\d+$/.test(height)) return null;
  return BigInt(height);
}

function collectMissingEnvNames(value: JsonValue, names = new Set<string>()): string[] {
  if (Array.isArray(value)) {
    for (const entry of value) collectMissingEnvNames(entry, names);
  } else if (value !== null && typeof value === "object") {
    for (const [key, entry] of Object.entries(value)) {
      if (key === "missingEnvNames" || key === "missingProductionEnvNames") {
        for (const name of asArray(entry as JsonValue)) names.add(String(name));
      } else {
        collectMissingEnvNames(entry as JsonValue, names);
      }
    }
  }
  return [...names].sort();
}

function methodEntry(discovery: Record<string, JsonValue>, methodName: string): Record<string, JsonValue> | null {
  const methods = asArray(discovery.methods).map((method) => asRecord(method));
  return methods.find((method) => method.method === methodName) ?? null;
}

function accountIdFromBalance(row: Record<string, JsonValue>): string | null {
  const nestedBalance = asRecord(row.balance ?? {});
  const source = stringValue(row.source ?? null);
  const status = stringValue(row.status ?? null);
  if (source !== "local-runtime-balance" && status !== "local_runtime") return null;
  return stringValue(nestedBalance.accountId ?? null) ?? stringValue(row.walletAddress ?? null);
}

function amountFromBalance(row: Record<string, JsonValue>): bigint {
  const amount = stringValue(asRecord(row.balance ?? {}).units ?? row.amount ?? "0") ?? "0";
  return /^\d+$/.test(amount) ? BigInt(amount) : 0n;
}

function sleep(ms: number) {
  return new Promise((resolveSleep) => setTimeout(resolveSleep, ms));
}

async function waitForHeightAdvance(client: FlowChainClient, firstStatus: Record<string, JsonValue>, timeoutMs: number): Promise<Record<string, JsonValue>> {
  const firstHeight = heightValue(firstStatus);
  if (firstHeight === null) return firstStatus;
  const deadline = Date.now() + timeoutMs;
  let latestStatus = firstStatus;
  while (Date.now() < deadline) {
    await sleep(1000);
    latestStatus = asRecord(await client.chainStatus());
    const latestHeight = heightValue(latestStatus);
    if (latestHeight !== null && latestHeight > firstHeight) return latestStatus;
  }
  return latestStatus;
}

function repoRoot() {
  return resolve(dirname(fileURLToPath(import.meta.url)), "..", "..", "..");
}

function writeGeneratedReference(discovery: Record<string, JsonValue>, outputPath: string) {
  const methods = asArray(discovery.methods)
    .map((method) => asRecord(method))
    .sort((left, right) => String(left.method ?? "").localeCompare(String(right.method ?? "")));
  const lines = [
    "# FlowChain RPC Reference",
    "",
    "Generated by `npm run flowchain:dev-pack:e2e` from live `rpc_discover` output.",
    "",
    `- RPC path: \`${String(discovery.rpcPath ?? "/rpc")}\``,
    `- Chain id: \`${String(discovery.chainId ?? "unknown")}\``,
    `- Deployment mode: \`${String(discovery.deploymentMode ?? "unknown")}\``,
    `- Completion gate passed: \`${String(discovery.productionReady ?? false)}\``,
    `- Public RPC gate passed: \`${String(discovery.publicRpcReady ?? false)}\``,
    "",
    "| Method | Category | Mode | Public eligible |",
    "| --- | --- | --- | --- |",
    ...methods.map((method) => {
      return `| \`${String(method.method ?? "")}\` | ${String(method.category ?? "")} | ${String(method.mode ?? "")} | ${String(method.publicRpcEligible ?? false)} |`;
    }),
    "",
    "Public exposure still depends on the public deployment contract and owner inputs. This reference is not a public-ready claim.",
    "",
  ];
  writeFileSync(outputPath, lines.join("\n"), "utf8");
}

function rpcOriginFromUrl(rpcUrl: string): string {
  try {
    const url = new URL(rpcUrl);
    url.pathname = "";
    url.search = "";
    url.hash = "";
    return url.toString().replace(/\/$/, "");
  } catch {
    return "http://127.0.0.1:8787";
  }
}

function discoveryMethodNames(discovery: Record<string, JsonValue>): string[] {
  return asArray(discovery.methods)
    .map((method) => stringValue(asRecord(method).method))
    .filter((method): method is string => method !== null && method.length > 0)
    .sort((left, right) => left.localeCompare(right));
}

